package Aufgabe_2.Tier_und_Pflanzen;

public class Pflanze {

    private String name;
    private String fabe;

    public Pflanze(String name, String fabe) {
        this.name = name;
        this.fabe = fabe;
    }



    public String getName() {
        return name;
    }
    public String getFabe() {
        return fabe;
    }
}
